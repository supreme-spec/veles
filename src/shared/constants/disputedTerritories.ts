/** Быстрый доступ по slug. */
export const DISPUTED_TERRITORY_BY_SLUG: Record<string, DisputedTerritory> =
  DISPUTED_TERRITORIES.reduce((acc, t) => {
    acc[t.slug] = t;
    return acc;
  }, {} as Record<string, DisputedTerritory>);

/** true, если slug — спорная/частично признанная территория. */
export function isDisputedTerritory(slug: string): boolean {
  return Boolean(DISPUTED_TERRITORY_BY_SLUG[slug]);
}

/** Возвращает политический статус для slug или undefined для обычных стран. */
export function getPoliticalStatus(slug: string): PoliticalStatusValue | undefined {
  return DISPUTED_TERRITORY_BY_SLUG[slug]?.politicalStatus;
}

/** Возвращает поясняющее примечание для slug или пустую строку. */
export function getPoliticalStatusNote(slug: string): string {
  return DISPUTED_TERRITORY_BY_SLUG[slug]?.note || '';
}
