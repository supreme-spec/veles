/**
 * Возвращает русское название по латинскому слагу и типу сущности.
 * Приоритет: словарь > красивый фолбэк.
 */
export function getRussianNameBySlug(slug: string, type: 'city' | 'region' | 'country' = 'city'): string {
  const normalized = slug.toLowerCase().trim();

  if (SLUG_TO_RUSSIAN_NAME[normalized]) {
    return SLUG_TO_RUSSIAN_NAME[normalized];
  }

  const fallback = normalized
    .replace(/[-\s]+/g, ' ')
    .split(' ')
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ');

  if (type === 'region' || type === 'country') {
    return fallback;
  }

  return fallback;
}
